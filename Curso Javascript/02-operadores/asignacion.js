// Operadores de asignación

let x = 5;

// Operador de adición +=
console.log("adición", x += 5);

// Operador de asignaación de resta -=
console.log("disminución", x -= 2);

// Operador de asignación de multiplicación *=
console.log("multiplicación", x *= 2);

// Operador de asignación de división /=
console.log("división", x /= 2);

// Operador de asignación de modulo o residuo %=
console.log("residuo", x %= 2);

// Operador de asignación por potenciación **=
console.log("potenciación", x **= 2);




