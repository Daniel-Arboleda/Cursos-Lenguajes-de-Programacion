// Operadores comparación 

let x = 2;
let y = 5;

// Comparación mayor que  >
console.log("mayor que", x > y);
console.log("mayor que", y > y);
console.log("mayor que", y > x);

// Comparación menor que <
console.log("menor que", x < y);
console.log("menor que", x < x);
console.log("menor que", y < x);

// Comparación mayor o igual que >=
console.log("mayor o igual que", x >= y);
console.log("mayor o igual que", x >= x);
console.log("mayor o igual que", y >= x);

// Comparación menor o igual que <=
console.log("menor o igual que", x <= y);
console.log("menor o igual que", x <= x);
console.log("menor o igual que", y <= x);

// Comparación operadores de igualdad ==
console.log ("igualdad", x == y);
console.log ("igualdad", x == 2);
console.log ("igualdad", x == "2");

// Comparación de negación !=
console.log("negación", x != x);
console.log("negación", x != y);

// Comparación de igualdad obasoluta ===
console.log("igualdad absoluta", x === x);
console.log("igualdad absoluta", x === 2);
console.log("igualdad absoluta", x === '2');








