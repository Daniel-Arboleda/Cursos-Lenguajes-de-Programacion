// Operador aritmetico de igualdad

let x = 5;
let z = 2;
let y = "Hola mundo";

console.log(x);
console.log(y);


// Operadores aritmeticos de suma
console.log("suma", x + z);

// Operador aritmetico de resta
console.log("resta", x - z);

// Operador aritmetico de multiplicación
console.log("multiplicación", x * z);

// Operador aritmetico de división
console.log("división", x / z);

// Operador aritmetico de modulo o residuo
console.log("modulo", x % z);

// Operador aritmetico de potenciación
console.log("potenciación", x ** z);


// Operador aritmetico de incremento ++x, x++
console.log("incremento ++a", ++x);
console.log("incremento a++", x++);

// Operador aritmetico de resta --x, x--
console.log("resta --a", --z);
console.log("resta --a", z--);









